package myqualityassurance.de;

public class Operations {
    public long addition(long a, long b) {
        return a + b;
    }
    public long subtraction(long a, long b) {
        return a - b;
    }
    public long multiplication(long a, long b) {
        return a * b;
    }
    public long division(long a, long b) {
        return a / b;
    }
}
